export default interface Option{
    /**
     * radius of the circle (pixels)
     */
    r: number
    /**
     * stroke width (pixels)
     */
    swidth: number
    /**
     * circle stroke width (pixels)
     */
    cwidth: number
    /**
     * stroke color (same format as in css)
     */
    stroke: string
    /**
     * accent color (same format as in css)
     */
    accent: string
    /**
     * background color (same format as in css)
     */
    background: string
}
